package loops;

public class TypeIfElse1 {
	public static void main(String[] args) {

		int number = 10;

		if (number < 0) {
			System.out.println("The number is Positive");
		}

		else {
			System.out.println("The number is Negative");
		}

		System.out.println("This is If Else Statement");
	}

}
