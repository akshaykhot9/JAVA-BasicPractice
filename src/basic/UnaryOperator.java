package basic;

public class UnaryOperator {

	public static void main(String[] args) {
		int i = 10;
		System.out.println(i);
		System.out.println(i++);
		System.out.println(i);
		System.out.println(i--);
		System.out.println(i);

		int y = 20;
		System.out.println(y);
		System.out.println(++y);
		System.out.println(y);
		System.out.println(--y);
		System.out.println(y);

	}
}
